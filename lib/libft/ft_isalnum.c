#include "libft.h"

int	ft_isalnum(int c)
{
	if (c < 48)
		return (0);
	if (c >= 58 && c <= 64)
		return (0);
	if (c >= 91 && c <= 96)
		return (0);
	if (c > 122)
		return (0);
	return (1);
}
